from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from fastapi.staticfiles import StaticFiles
from fastapi.responses import FileResponse
import os
from models.schemas import ChatRequest, ChatResponse
from services.ai_service import AIService

app = FastAPI(title="Owe MVP API", description="AI-powered civic reciprocity platform")

# Initialize AI Service
ai_service = AIService()

# Enable CORS for local development
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

@app.get("/api/health")
def healthcheck():
    """Healthcheck endpoint for deployment verification."""
    return {"status": "ok"}

@app.get("/api/brief")
def get_brief():
    """Returns the daily community brief synthesized by AI service."""
    try:
        return ai_service.get_daily_brief()
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Failed to generate brief: {str(e)}")

@app.get("/api/missions")
def get_missions():
    """Returns the list of active community missions generated by AI service."""
    try:
        return ai_service.get_missions()
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Failed to fetch missions: {str(e)}")

@app.post("/api/chat", response_model=ChatResponse)
def chat_with_civic_ai(request: ChatRequest):
    """
    Conversational civic intelligence endpoint.
    Handles querying live datasets via prompt/Gemini structures.
    """
    try:
        response = ai_service.chat(request.message)
        return ChatResponse(reply=response["reply"])
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Chat error: {str(e)}")

# --- Serving Frontend (For Docker Single Container SPA setup) ---
FRONTEND_BUILD_DIR = os.path.join(os.path.dirname(os.path.dirname(__file__)), "frontend", "dist")

if os.path.exists(FRONTEND_BUILD_DIR):
    # Mount specific assets directory for compiled JS/CSS
    assets_dir = os.path.join(FRONTEND_BUILD_DIR, "assets")
    if os.path.exists(assets_dir):
        app.mount("/assets", StaticFiles(directory=assets_dir), name="assets")

    # SPA Fallback for all other routes
    @app.get("/{fallback_path:path}")
    async def catch_all(fallback_path: str):
        if fallback_path.startswith("api"):
            raise HTTPException(status_code=404, detail="API route not found")
        
        # Check if the file is directly in the root of frontend/dist (e.g. favicon.ico, manifest.json)
        file_path = os.path.join(FRONTEND_BUILD_DIR, fallback_path)
        if os.path.exists(file_path) and os.path.isfile(file_path):
            return FileResponse(file_path)
            
        # Default fallback to React index.html
        return FileResponse(os.path.join(FRONTEND_BUILD_DIR, "index.html"))
else:
    @app.get("/{fallback_path:path}")
    def fallback(fallback_path: str):
        if fallback_path.startswith("api"):
            raise HTTPException(status_code=404, detail="API route not found")
        return {"message": "API is running. Frontend build not found. If running locally with Vite, use the Vite dev server (port 5173)."}
